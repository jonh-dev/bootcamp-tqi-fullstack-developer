public class Calculadora {
    // Criando método soma.
    public static void soma(double numero1, double numero2) {
      // Código para efetuar a soma entre 2 parâmetros.
      double resultado = numero1 + numero2;
      // Executando a soma entre 2 parâmetros e exibindo o resultado no console.
      System.out.println("A soma de " + numero1 + " mais " + numero2 + " é " + resultado);
    }
    // Criando o método subtração.
    public static void subtracao(double numero1, double numero2) {
      // Código para efetuar a subtração entre 2 parâmetros.
      double resultado = numero1 - numero2;
      // Executando a subtração entre 2 parâmetros e exibindo o resultado no console.
      System.out.println("A subtração de " + numero1 + " menos " + numero2 + " é " + resultado);
    }

    // Criando o método subtração.
    public static void multiplicacao(double numero1, double numero2) {
      // Código para efetuar a subtração entre 2 parâmetros.
      double resultado = numero1 * numero2;
      // Executando a subtração entre 2 parâmetros e exibindo o resultado no console.
      System.out.println("A multiplicação de " + numero1 + " vezes " + numero2 + " é " + resultado);
    }

    // Criando o método subtração.
    public static void divisao(double numero1, double numero2) {
      // Código para efetuar a subtração entre 2 parâmetros.
      double resultado = numero1 / numero2;
      // Executando a subtração entre 2 parâmetros e exibindo o resultado no console.
      System.out.println("A divisão de " + numero1 + " por " + numero2 + " é " + resultado);
    }
}